int	ft_iterative_power(int nb, int power)
{
	int	i;
	int	n;

	i = power;
	n = nb;
	while (i > 1)
	{
		n *= nb;
		i--;
	}
	if (i == 0)
		return (1);
	if (i < 0)
		return (0);
	return (n);
}
